import UIKit

extension UIFont {
    
    static var demo_regularFont: UIFont {
        return UIFont(name: "BloggerSans", size: 18)!
    }
    
    static var demo_regularBoldFont: UIFont {
        return UIFont(name: "BloggerSans-Bold", size: 18)!
    }
    
    static var demo_smallFont: UIFont {
        return UIFont(name: "BloggerSans", size: 12)!
    }
    
    static var demo_smallBoldFont: UIFont {
        return UIFont(name: "BloggerSans-Bold", size: 12)!
    }
    
    static var demo_titleFont: UIFont {
        return UIFont(name: "BloggerSans-Medium", size: 22)!
    }
    
}